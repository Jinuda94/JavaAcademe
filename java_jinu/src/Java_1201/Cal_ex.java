package Java_1201;

public class Cal_ex {

	public static void main(String[] args) {
		int i = 10;
		int j = 20;
		
		int sum = i+j;
		int sub = i-j;
		int mul = i*j;
		double div = (double)i/(double)j;
		
		System.out.println("덧셈: "+sum);
		System.out.println("뺼셈: "+sub);
		System.out.println("곱하기: "+mul);
		System.out.println("나누기: "+div);

	}

}

