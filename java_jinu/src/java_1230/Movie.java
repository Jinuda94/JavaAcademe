package java_1230;

public abstract class Movie {
	String movie_name;
	String movie_RunTime;
	String movie_actor;
	
	public abstract String getMovie_name();
	public abstract void setMovie_name(String movie_name);
	public abstract String getMovie_RunTime();
	public abstract void setMovie_RunTime(String movie_RunTime);
	public abstract String getMovie_actor();
	public abstract void setMovie_actor(String movie_actor);
	
}
